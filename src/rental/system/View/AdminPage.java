    private void analysisProperties(Vector<Property> ps) {

        int numberOfInactive = 0, numberOfava = 0, numberOfBooked = 0, numberOfTActive = 0;

        for (int i = 0; i < ps.size(); i++) {
            if (ps.get(i).getActivation().equals("Inctive")) {
                numberOfInactive++;
            }
            if (ps.get(i).getBook().equals("Available")) {
                numberOfava++;
            }
            if (ps.get(i).getBook().equals("Booked")) {
                numberOfBooked++;
            }
            if (ps.get(i).getActivation().equals("Active")) {
                numberOfTActive++;
            }
        }
        inactiveNumber.setText(String.valueOf(numberOfInactive));
        avaNumber.setText(String.valueOf(numberOfava));
        bookedNumber.setText(String.valueOf(numberOfBooked));
        activeNumber.setText(String.valueOf(numberOfTActive));
        totalPropertiesNumber.setText(String.valueOf(ps.size()));

    }

    private Property getSelectedProperty(JTable t) {
        int index = t.getSelectedRow();
        TableModel model = t.getModel();
        if (index >= 0) {
            int id = Integer.parseInt(model.getValueAt(index, 0).toString());

            for (int i = 0; i < holdTheDataOfProperties.size(); i++) {
                if (holdTheDataOfProperties.get(i).getId() == id) {
                    putOldImage(holdTheDataOfProperties.get(i).getPhoto());
                    return property = holdTheDataOfProperties.get(i);
                }
            }
        }
        return new Property();
    }

