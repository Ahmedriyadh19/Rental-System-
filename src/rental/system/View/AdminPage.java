    private void analysisUsers(Vector<User> us) {
        int numberOfAdmin = 0, numberOfAgent = 0, numberOfOwner = 0, numberOfTenant = 0;

        for (int i = 0; i < us.size(); i++) {
            if (us.get(i).getUserType().equals("Admin")) {
                numberOfAdmin++;
            }
            if (us.get(i).getUserType().equals("Agent")) {
                numberOfAgent++;
            }
            if (us.get(i).getUserType().equals("Owner")) {
                numberOfOwner++;
            }
            if (us.get(i).getUserType().equals("Tenant")) {
                numberOfTenant++;
            }
        }
        adminNumber.setText(String.valueOf(numberOfAdmin));
        agentNumber.setText(String.valueOf(numberOfAgent));
        ownerNumber.setText(String.valueOf(numberOfOwner));
        tenantNumber.setText(String.valueOf(numberOfTenant));
        totalNumberofUsers.setText(String.valueOf(us.size()));
    }

